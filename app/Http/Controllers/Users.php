    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        // Validasi data
        $request->validate([
            'name' => 'required|string|max:255|unique:roles',
            'permissions' => 'required|array',
            'permissions.*' => 'exists:permissions,id',
        ]);

        // Simpan role baru
        $role = Roles::create([
            'uuid' => Str::uuid(),
            'name' => $request->name,
        ]);

        // Attach permissions yang dipilih ke role baru
        $role->permissions()->attach($request->permissions, ['created_at' => now()]);

        // Redirect atau kembalikan response sesuai kebutuhan
        return redirect()->route('roles.index')->with('success', 'Role baru berhasil ditambahkan.');
    }

